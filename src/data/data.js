// ─── Accountability Ratings ───────────────────────────────────────────────────
// Note: 2020 ratings were waived due to COVID-19.
// Rating scale: A=Excellent, B=Meets Expectations, C=Average, D=Below Average, F=Unsatisfactory

function genRatings(seed) {
  const baseScores = {
    A: [80, 100], B: [65, 79], C: [50, 64], D: [35, 49], F: [0, 34]
  };
  const letters = ['F','D','C','C','B','B','A','A','A'];
  function rng(n) { return (n * 1664525 + 1013904223) & 0x7fffffff; }
  function scoreForLetter(l, r) {
    const [lo, hi] = baseScores[l];
    return (lo + (r % (hi - lo + 1))).toFixed(1);
  }
  let r = rng(seed);
  const startLetter = letters[r % 9];
  const years = [2018, 2019, 2021, 2022, 2023, 2024];
  const cats = { A:'Excellent', B:'Meets Expectations', C:'Average', D:'Below Average', F:'Unsatisfactory' };
  return years.map((year, i) => {
    r = rng(r + i);
    const offset = ((r % 3) - 1); // -1, 0, or 1 letter shift
    const idx = Math.max(0, Math.min(8, letters.indexOf(startLetter) + offset * (i > 2 ? 1 : 0)));
    const letter = letters[idx];
    r = rng(r);
    const score = parseFloat(scoreForLetter(letter, r));
    return { year, rating: letter, score, category: cats[letter] };
  });
}

