// ─── Achievement Data ─────────────────────────────────────────────────────────
// SC READY: ELA and Math grades 3-8
// SC PASS: Science grades 4, 6, 8
// EOCEP: High school end-of-course exams
// Performance levels: Below Basic, Basic, Meets, Exceeds

function pct(seed, base, spread) {
  function rng(n) { return (n * 22695477 + 1) & 0xffffffff; }
  const r = rng(seed);
  const v = base + ((r % (spread * 2 + 1)) - spread);
  return Math.max(5, Math.min(85, v));
}

function makeAchievementBands(schoolId, gradeOrCourse, year, subject) {
  const hash = [...(schoolId + gradeOrCourse + year + subject)].reduce((a, c) => a * 31 + c.charCodeAt(0), 0);
  function rng(n) { return Math.abs((n * 1664525 + 1013904223) | 0); }
  let h = rng(hash);
  const meets = pct(h, 35, 12);
  h = rng(h);
  const exceeds = pct(h, 20, 8);
  h = rng(h);
  const belowBasic = pct(h, 20, 10);
  const basic = 100 - meets - exceeds - belowBasic;
  return {
    belowBasic: Math.max(3, belowBasic),
    basic: Math.max(3, basic),
    meets,
    exceeds
  };
}

